/*  Que son los modulos

    Los modulos son piezas de codigo que podemos escribir en ficheros independientes. 
    Los módulos pueden tener código, como clases, funciones, objetos o simples datos primitivos, 
    que se puede importar desde otros archivos.

        ¿ Como usar modulos desde el archivo HTML ?

            Lo primero que hay que decir es que debes hacer una acción especial para que el navegador procese correctamente los archivos Javascript que usan módulos. 
            Básicamente se trata de advertir al navegador que el script Javascript usa módulos, para lo que necesitas traerte ese código desde el HTML usando una sintaxis 
            especial en la etiqueta SCRIPT. Simplemente tenemos que marcar con type="module" el script que queremos incluir.

*/